package com.bridgelabz.day1;

public class DefaultValues {
	int a;
	float b;
	double c;
	boolean d;
	long e;
	short f;
	byte g;
	char h;

	public static void main(String[] args) {
		DefaultValues obj = new DefaultValues();
		
		System.out.println("Default value of int : " + obj.a);
		System.out.println("Default value of float : " + obj.b);
		System.out.println("Default value of double : " + obj.c);
		System.out.println("Default value of boolean : " + obj.d);
		System.out.println("Default value of long : " + obj.e);
		System.out.println("Default value of short: " + obj.f);
		System.out.println("Default value of byte : " + obj.g);
		System.out.println("Default value of char : " + obj.h);
	}
}
